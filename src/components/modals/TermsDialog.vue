        the full amount of the Digital Asset/s to the Wallet of the Borrower.
      </p>
      <p>
        7. Each Payment or Repayment is allowed only in the LPN of the Lease.
      </p>
      <p>
        <strong>
          IX. Liability Levels & Re-evaluation
        </strong>
      </p>
      <p>
        1. All Users hereby acknowledge that the sustainability of the Platform can only be kept as far as an adequate
        balance is always maintained between the Current Price of the Asset (which functions also as Collateral) and the
        current аmount of the Lease (LTV).
      </p>
      <p>
        2. Therefore, the Protocol shall maintain LTV level indicators, which ensure that the аmount of the Lease is
        always kept beneath Max LTV, or otherwise Liquidation under Section D.X is started by the Protocol.
      </p>
      <p>
        3. In order to assess the LTV, the Protocol shall regularly procure Re-evaluation., which shall check and reassess
        the Current Asset Value. Re-evaluation shall be based on data gathered from the Market Data Feed.
      </p>
      <p>
        4. Based on the Re-evaluation, the Protocol shall monitor the LTV regarding certain Lease.
      </p>
      <p>
        5. When the Asset depreciates, the Protocol shall monitor if the LTV is close to reaching either Healthy LTV
        Liability level or Max LTV.
      </p>
      <p>
        6. In the case under Section D.IX.5. the Protocol shall send to the Borrower Liquidation warning, which will
        contain information on:
      </p>
      <p>
        6.1. the Current Asset Value;
      </p>
      <p>
        6.2. the current LTV regarding the Lease;
      </p>
      <p>
        6.3. information on the amount of the Margin Payment the Borrower should make in order to reach Healthy LTV for
        the Lease;
      </p>
      <p>
        7. Based on the Liquidation warning, the Borrower shall be able to make Margin Payment in LPN by transfer to
        corresponding Lease contract, in which the Collateral is locked.
      </p>
      <p>
        8. Should the Borrower fails to make the Margin Payment needed, the Protocol shall trigger the Liquidation process
        under Section D.X.
      </p>
      <p>
        9. For avoidance of any doubt, LTV levels shall be determined by the Protocol for each Lease Contract, based on
        mathematical configurations existing in the Protocol.
      </p>
      <p>
        <strong>
          X. Liquidation process
        </strong>
      </p>
      <p>
        1. Based on the procedure set in Section D.IX. the Protocol shall be entitled to trigger Liquidation in case the
        LTV reaches Max LTV and provided that the Borrower hasn’t made a sufficient Margin Payment for reaching Healthy
        LTV.
      </p>
      <p>
        2. In case of LTV reaches Max LTV, the Protocol shall, depending on the case, automatically procure Partial or
        Total Liquidation with respect to the Collateral.
      </p>
      <p>
        2.1. In case of Partial Liquidation the Protocol shall sell a portion of the Collateral, which will generate sales
        revenue to repay the current Lease liability until LTV reaches a Healthy LTV level.
      </p>
      <p>
        2.2. Partial Liquidation shall be triggered each time the LTV reaches the Max LTV.
      </p>
      <p>
        3. Total Liquidation shall be the final Liquidation process, in which the Protocol shall sell the balance of the
        Collateral and shall use the proceeds thereof to repay the outstanding Lease liability.
      </p>
