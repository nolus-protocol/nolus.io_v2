      <h1>
        SECTION D
      </h1>
      <h2>
        Lease
      </h2>
      <br>
      <p>
        <strong>
          I. DeFi Lease
        </strong>
      </p>
      <p>
        1. Subject to eligibility with predetermined Protocol parameters each User shall be entitled to use Leased crypto
        financing from the Protocol for the purchase of Digital Asset/s.
      </p>
      <p>
        2. All Leases shall be provided only in the Digital Asset relevant to the utilized LPP (LPN of Liquidity
        Provider’s Pools existent on the Protocol).
      </p>
      <p>
        <strong>
          II. Borrow Requests
        </strong>
      </p>
      <p>
        1. А User willing to receive Lease financing shall submit a Borrow Request to the Lease Factory in a standard
        form.
      </p>
      <p>
        2. The standard Borrow Request form requires the User to fill in the requested information as follows:
      </p>
      <p>
        2.1. Requested denomination of the Digital Asset to be borrowed;
      </p>
      <p>
        2.2. The Digital Asset, which shall be purchased with the Lease;
      </p>
      <p>
        2.3. Amount and denomination of the Digital Asset, which will serve as Down payment;
      </p>
      <p>
        3. If the Borrow Request is compliant with these Statues and the approved Protocol algorithms, the Protocol shall
        provide quote to the User, containing the main parameters of the Lease. Quotes shall also indicate expiry period,
        after which such quotes shall no longer be valid.
      </p>
      <p>
        4. Protocol shall not allow submission of any non – compliant Borrow Requests.
      </p>
      <p>
        <strong>
          III. Interest rate calculation
        </strong>
      </p>
      <p>
        1. The Interest rate will be formed for each respective Lease by Interest Algorithm integrated into the Protocol,
        which shall take into account various parameters, including but not limited to: level of Utilization credit
        history of the Borrower, amount of the Lease requested.
      </p>
      <p>
        2. Once formed under the rules of Section D.III.1. above and fixed in the Lease contract, the Interest rate is
        fixed for the entire duration of the Lease.
      </p>
      <p>
        3. Each User hereby fully agrees and accepts the Interest Algorithm as an independent programming tool determinant
        to the Interest rate both for Lease Interest and for Margin Interest at each respective moment.
      </p>
      <p>
        4. The Protocol shall provide lower Interest rates for Stakers willing to receive Lease financing based on their
        staking amount and duration.
      </p>
      <p>
        <strong>
          IV. Lease Contract
        </strong>
      </p>
      <p>
        1. In order to activate the Lease Contract, the Borrower shall have to confirm the respective quote and contribute
        the Down payment in accordance with the rules set in Section D.V below.
      </p>
      <p>
        2. The Lease Contract shall contain the following data:
      </p>
      <p>
        2.1. Borrower’s Wallet address;
      </p>
      <p>
        2.2. The LPN of the lent funds;
      </p>
      <p>
        2.3. Fixed Interest rate, applicable for the respective Lease;
      </p>
      <p>
        2.4. Initial LTV;
      </p>
      <p>
        2.5. Max LTV;
      </p>
      <p>
        2.6. Healthy LTV;
      </p>
      <p>
        2.7. Interest Period
      </p>
      <p>
        2.8. Due Date;
      </p>
      <p>
        2.9. Grace period;
      </p>
      <p>
        2.10. LPP address;
      </p>
      <p>
        3. To the extent the Interest is duly and timely paid and no Liquidation event occurs, the Lease shall be
        considered executed for an indefinite period and the date/s of full or partial Repayment of the Principal shall be
        at the discretion of the Borrower.
      </p>
      <p>
        4. Lease shall be effective until the occurrence of the earlier event between: (i) full Repayment of the Principal
        plus all outstanding Interest and all transaction fees, or (ii) Total Liquidation event. The effective period of
        the Lease shall also be referred to as the Tenure of the Lease.
      </p>
      <p>
        <strong>
          V. Down payment
        </strong>
      </p>
      <p>
        1. The amount of the Down payment of the User shall determine the maximum level of Lease such User will be allowed
        to open in the Protocol. The maximum level of the Lease corresponding to the Down payment level selected by the
        User, shall be displayed in the quote, generated by the Protocol.
      </p>
      <p>
        2. The User can make the Down payment in one of the following options:
      </p>
      <p>
        2.1. in the LPN currency;
      </p>
      <p>
        2.2. in another supported Digital Asset currency, different than the leased Digital Asset;
      </p>
      <p>
        2.3. in the same type of Digital Asset as the leased, in which case the Down payment shall directly be added to
        the Principal to form the Collateral.
      </p>
      <p>
        3. For avoidance of any doubt, irrespectively of the denomination of the Down payment, the entire liability of the
        Borrower shall be calculated, charged and due in the respective LPN currency.
      </p>
